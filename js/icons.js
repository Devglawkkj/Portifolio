/**
 * Renderizar ícone por nome
 */
export function renderIcon(name, additionalClass = "") {
  const icon = ICONS[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return "";
  }
  // Replace class attribute dynamically if needed
  return icon.replace('class="', `class="${additionalClass} `);
}

/**
 * Renderizar elemento com ícone
 */
export function createIconButton(iconName, href, ariaLabel) {
  const button = document.createElement("a");
  button.href = href;
  button.className =
    "w-12 h-12 rounded-xl bg-dark-primary border border-zinc-800 flex items-center justify-center text-zinc-400 hover:text-accent-cyan hover:border-accent-cyan/50 transition-all";
  button.setAttribute("aria-label", ariaLabel);
  button.innerHTML = ICONS[iconName] || "";
  return button;
}
